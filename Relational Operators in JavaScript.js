//Relational Operators in JavaScript
 
/*
>	greater than
<	less than
>=	greater than or equal to
<=	less than or equal to
*/
 
let a=10;
let b=20;

console.log("Greater : ",a>b);
console.log("Lesser  : ",a<b);
console.log("Greater Than Equal  : ",a>=b);
console.log("Greater Than Equal  : ",a<=b);